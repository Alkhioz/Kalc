public void trigonometria(){
switch(funcion){
    case "sin":
        valor1=Double.parseDouble(pantalla.getText());
        pantalla2.setText("sin("+valor1+" ) ");
        sen=Math.sin(Math.PI*(valor1)/180);
        pantalla.setText(sen+" ");
        break;
     case "cos":
        valor1=Double.parseDouble(pantalla.getText());
        pantalla2.setText("cos("+valor1+" ) ");
        cos=Math.cos(Math.PI*(valor1)/180);
        pantalla.setText(cos+" ");
        break;
    case "tan":
        valor1=Double.parseDouble(pantalla.getText());
        pantalla2.setText("tan("+valor1+" ) ");
        tan=Math.tan(Math.PI*(valor1)/180);
        pantalla.setText(tan+"");
        break;

}



}
   

//Botón de la función de igual
    private void Operaciones(double valor1, double valor2){
    switch(operacion){
        case "+":
            resultado=valor1+valor2;
            pantalla.setText(resultado +"");
            valor1=Double.parseDouble(pantalla.getText());
            break;
        case "-":
             resultado=valor1-valor2;
            pantalla.setText(resultado +"");
            valor1=Double.parseDouble(pantalla.getText());
            break;
        case "*":
             resultado=valor1*valor2;
            pantalla.setText(resultado +"");
            valor1=Double.parseDouble(pantalla.getText());
            break;
        case "/":
            if(valor2==0){
            pantalla.setText("SYNTAX ERROR ");
            break;
            }else{
             resultado=valor1/valor2;
            pantalla.setText(resultado +"");
            valor1=Double.parseDouble(pantalla.getText());
            break;
            }
     }
    }
